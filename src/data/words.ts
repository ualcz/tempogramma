export const getRandomWord = (theme: Theme, difficulty: number): WordData => {
  const themeWords = words.filter(
(word) => word.theme === theme && word.difficulty === difficulty
  );
  return themeWords[Math.floor(Math.random() * themeWords.length)];
};

export const scrambleWord = (word: string): string => {
  const array = word.split("");
  for (let i = array.length - 1; i > 0; i--) {
const j = Math.floor(Math.random() * (i + 1));
[array[i], array[j]] = [array[j], array[i]];
  }
  return array.join("");
};
